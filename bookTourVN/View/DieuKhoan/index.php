<h4><i><b>Giá tour bao gồm:</b></i></h4>
- Chi phí vận chuyển phụ thuộc vào số khách cấu thành 1 đoàn.<br>
- Chi phí khách sạn<br>
- Thời gian nhận phòng là 14h00  và trả phòng là 12h00 ngày hôm sau. Tiêu chuẩn phòng là 2 người/phòng.<br>
- Bảo hiểm du lịch.<br>
- HDV hỗ trợ và hướng dẫn suốt tuyến.<br>

<h4><i><b>Giá tour không bao gồm:</b></i></h4>
- Ăn uống các bữa trong tour.<br>
- Chi phí tham quan, vé vào cổng.<br>
- Chi phí mua sắm ngoài chương trình.<br>
- Thuế VAT và phí phục vụ.<br>
- Phụ thu phòng đơn theo giá công bố của khách sạn.<br>
- Vé máy bay và các chi phí vận chuyển khác (tàu, thuyền,...)<br>

<h4><i><b>Trẻ em:</b></i></h4>
- Đối với trẻ em dưới 5 tuổi:<br>
+ Miễn phí vé, ngủ theo bố mẹ, người thân, được 1 ghế ngồi đôi với bé đầu tiên.<br>

+ 50% vé đối với trẻ thứ 2, được hưởng quyền lợi như người lớn, ngủ chung phòng với bố mẹ được kê thêm extra bed.<br>

+ Đối với trẻ em 6-10 tuổi: 50% vé ,được hưởng quyền lợi như người lớn, ngủ chung phòng với bố mẹ được kê thêm extra bed.

+ Đối với trẻ em trên 10 tuổi: tính phí như người lớn.<br>

